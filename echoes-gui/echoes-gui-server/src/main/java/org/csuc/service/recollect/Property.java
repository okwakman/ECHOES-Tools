package org.csuc.service.recollect;

/**
 * @author amartinez
 */
public class Property {

    private String key;
    private String value;

    public Property() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
